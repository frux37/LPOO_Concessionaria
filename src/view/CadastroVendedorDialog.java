     public void setVendedor(Vendedor v){
        this.vendedor = v;
        txtNome.setText(v.getNome());
        txtCPF.setText(v.getCPF());
    
        if (v.getDataNascimento() != null) {
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            txtDataNascimento.setText(v.getDataNascimento().format(formatter));
        }
    
        txtTelefone.setText(v.getTelefone());
        txtSalario.setText(String.valueOf(v.getSalario()));
        txtComissao.setText(String.valueOf(v.getComissao()));   
    }

