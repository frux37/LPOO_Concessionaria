    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        
        String nome = txtNome.getText();
        String CPF = txtCPF.getText();
        LocalDate dataNascimento = LocalDate.parse(txtDataNascimento.getText(), formatter);
        String telefone = txtTelefone.getText();
        double salario = Double.parseDouble(txtSalario.getText());
        double comissao = Double.parseDouble(txtComissao.getText());
        
        vendedor.setNome(nome);
        vendedor.setCPF(CPF); 
        vendedor.setDataNascimento(dataNascimento);   
        vendedor.setTelefone(telefone);
        vendedor.setSalario(salario);
        vendedor.setComissao(comissao);

        confirmado = true;
        JOptionPane.showMessageDialog(null, "vendedor Cadastrado com sucesso!\n" + vendedor);
        dispose();
    }//GEN-LAST:event_btnCadastrarActionPerformed

